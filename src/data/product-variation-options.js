/**
 * @param {string[]} values
 * @returns {string[]}
 */
export function normalizeShipExZoneValues(values) {
  if (!Array.isArray(values)) {
    return [];
  }

  return values
    .map((value) => PRODUCT_SHIP_EX_ZONE_VALUE_ALIASES[String(value).trim()] || String(value).trim())
    .filter((value) => value && value.toLowerCase() !== "none");
}

